package chapter12;

public class AutoBox5 {

    public static void main(String[] args) {

        Integer iob = 2;

        switch (iob) {
            case 1:
                System.out.println("oдин");
                break;
            case 2:
                System.out.println("двa");
                break;
            default:
                System.out.println("oшибкa");
        }
    }
}
